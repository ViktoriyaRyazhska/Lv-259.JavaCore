package task1;

public class Month {
	
	private int numOfMonth;
	private int day;
	
	public Month(int numOfMonth, int day) {
		this.numOfMonth = numOfMonth;
		this.day = day;
	}

	public int getNumOfMonth() {
		return numOfMonth;
	}

	public void setNumOfMonth(int numOfMonth) {
		this.numOfMonth = numOfMonth;
	}

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}
}
